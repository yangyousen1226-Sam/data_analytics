# 创建字符串
s = 'hello,world'
print(s[0])
print(s[4])
print(s[-1])
# 切片： 变量名[起始索引：结束索引位置+1：步数]
# 步数默认为1，可省略不写
# 起始索引默认为0，也可省略不写
# 结束索引默认为0，也可省略不写
# 结束索引为-1为右边开始，但输出时，不包括那位，为0就全输出

print('---------------------------')
""""
包头不包尾，第一个数字就是原本的位置数，后面一个要加一,
实际下面一个打印出来是he，而不是hl"""
print(s[0:2])  #包头不包尾（就是输出的内容包括开头索引的位置开始，不包尾就是不包含第二项数字索引结束填的那个数的位置的数）
print(s[6:9])  #wor
print('---------------------------')
s2 = "123456789"
print(s2[0:9:2])  #13579 最后面的2代表隔几个位索引
print(s2[:9:2])  #13579
print(s2[::2])  #13579
print('---------------------------')
# 练习（字符串反转）
print(s2[-1:-10:-1])   # 987654321反着数，跟上面相反
print(s2[::-1])   # 987654321


